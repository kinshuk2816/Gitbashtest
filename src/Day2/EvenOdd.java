package Day2;

import java.util.Scanner;

public class EvenOdd {

	public static void main(String[] args) {

		
		  Scanner w = new Scanner(System.in);

	        System.out.print("Enter a number: ");
	        int num = w.nextInt();

	        if(num % 2 == 0)
	            System.out.println( "even");
	        else
	            System.out.println( "odd");
		 
		 
		 
		 
	}

}
